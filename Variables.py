# Integer
age = 30

# Float
height = 5.9

# String
name = "Alice"

# Boolean
is_student = True

# List
fruits = ["apple", "banana", "cherry"]

# Tuple
coordinates = (10.0, 20.0)

# Dictionary
person = {
    "name": "Alice",
    "age": 30,
    "is_student": True
}

# Display variable types
print(type(age), type(height), type(name), type(is_student), type(fruits), type(coordinates), type(person))