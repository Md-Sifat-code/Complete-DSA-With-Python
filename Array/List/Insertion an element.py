# Explanation
print("Syntax: list.insert(index, value)")
print("The insert() method adds a value to the list at the specified index.")

# Example code
my_list = [1, 2, 4, 5]  # Initial list
print("Initial list:", my_list)

# Insert a new value at index 2
my_list.insert(2, 3)  # Inserts the value 3 at index 2
print("List after inserting 3 at index 2:", my_list)
